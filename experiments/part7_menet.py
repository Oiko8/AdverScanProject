# ─────────────────────────────────────────────────────────────────────────────
#   Part 7 — Model 4 (ME-Net) through Stages 1, 2, D4, Stage 5 (EOT).
#
#   What this is testing:
#     ME-Net's randomized preprocessing causes gradient masking. The
#     diagnostic pipeline should detect this in three ways:
#       1. Stage 1 (vanilla PGD) reports "high" robust accuracy →
#          false robustness signal when compared with Stage 5.
#       2. Stage 2 (AutoAttack) component breakdown shows Square Attack
#          finding more failures than APGD-CE → D1 SECONDARY fires.
#       3. PGD loss trajectory is non-monotonic → D4 fires.
#     And then Stage 5 (EOT-PGD) demonstrates the true robust accuracy.
#
#   What we expect to see on Model 4:
#     - PGD eps=8/255    : substantial robust accuracy (false positive)
#     - AutoAttack eps=8/255 : Square dominates APGD components
#     - EOT-PGD eps=8/255: near zero robust accuracy
#     - Gap (Stage 1 - Stage 5) = the headline diagnostic number
# ─────────────────────────────────────────────────────────────────────────────


# ── Config ───────────────────────────────────────────────────────────────────

# Must match the values used when training Model 4. The defaults here
# follow the paper's standard config and match models/train_menet.py.
# P_KEEP = 0.5
# RANK   = 10
P_KEEP = float(sys.argv[1]) if len(sys.argv) > 1 else 0.5
RANK   = int(sys.argv[2])   if len(sys.argv) > 2 else 10

# Model 4 checkpoint — generated by `python -m models.train_menet`.
P_TAG = f"p{int(P_KEEP*100):02d}"
R_TAG = f"r{RANK:02d}"
CKPT_PATH = os.path.join(
    MODEL_DIR, f"model4_menet_base_{P_TAG}_{R_TAG}_resnet50.pth"
)

# Evaluation epsilons. Subset of the project's standard EPSILONS — we
# want enough range to plot Stage 1 vs Stage 5, but EOT is expensive
# (40 forwards per step × steps × restarts), so we don't sweep
# every value.
EVAL_EPSILONS = [2/255, 4/255, 8/255]

# EOT config — Tramèr §15 protocol.
EOT_K_SAMPLES = 40
EOT_N_EVAL    = 20      # majority-vote forward passes when scoring


# ─────────────────────────────────────────────────────────────────────────────
#   Stage runners
# ─────────────────────────────────────────────────────────────────────────────

def stage1_pgd(model, test_loader, epsilons):
    """
    Stage 1 — vanilla PGD across epsilons. On ME-Net this is the
    "false robustness" measurement: the noisy single-pass gradient
    keeps PGD from finding the adversarial examples that exist.
    Returns dict {eps_raw → robust accuracy (%)}.

    Also returns a fresh-mask majority-vote clean accuracy at eps=0
    so that the eps=0 row of the report is comparable to the other rows
    (single-forward clean accuracy on a stochastic model is too noisy
    to be meaningful).
    """
    print("\n" + "=" * 65)
    print("  Stage 1 — vanilla PGD on Model 4 (ME-Net)")
    print("=" * 65)

    results = {}
    model.eval()

    for eps in epsilons:
        eps_label = f"{round(eps*255)}/255"

        if eps == 0:
            # Majority-vote clean accuracy.
            acc = _majority_vote_accuracy(model, test_loader,
                                           epsilon=0,
                                           num_samples=EVAL_NUM_SAMPLES)
            print(f"  epsilon = {eps_label}  ->  clean acc (maj. vote) = {acc:.1f}%")
            results[eps] = acc
            continue

        eps_scaled = scale_epsilon(eps)

        correct = 0
        total   = 0
        for images, labels in test_loader:
            if total >= EVAL_NUM_SAMPLES:
                break
            remaining = EVAL_NUM_SAMPLES - total
            images, labels = images[:remaining].to(DEVICE), labels[:remaining].to(DEVICE)

            adv = pgd_attack(model, images, labels, eps_scaled,
                             steps=PGD_STEPS, restarts=PGD_RESTARTS)
            # Evaluate the adversarial under majority vote — stochastic
            # model, same fairness as Stage 5.
            preds = _majority_vote_predict(model, adv, n_eval=EOT_N_EVAL)
            correct += preds.eq(labels).sum().item()
            total   += labels.size(0)

        acc = 100.0 * correct / total
        print(f"  epsilon = {eps_label}  ->  PGD robust acc = {acc:.1f}%")
        results[eps] = acc

    return results


def stage2_autoattack(model, test_loader, epsilons):
    """
    Stage 2 — AutoAttack at each epsilon. Returns
        accuracies : dict {eps_raw → AA accuracy}
        components : dict {eps_raw → {component → accuracy}}

    Note on stochastic models: AutoAttack standard mode treats the
    model as deterministic. On ME-Net this still works in the sense
    that components run end-to-end, but per-run numbers will fluctuate
    a few percent from random mask draws. The diagnostic we care about
    here — Square Attack outperforming APGD — is robust to that noise.

    A stricter evaluation would use AutoAttack's `version="rand"` mode
    which adds an EOT loop around the white-box components. Worth
    swapping in if D1 secondary status is borderline.
    """
    print("\n" + "=" * 65)
    print("  Stage 2 — AutoAttack on Model 4 (ME-Net)")
    print("=" * 65)

    accuracies = {}
    components = {}

    for eps in epsilons:
        eps_label = f"{round(eps*255)}/255"
        print(f"\n── AutoAttack at epsilon = {eps_label} ──────────────────")

        acc, comps = run_autoattack(
            model, test_loader, epsilon=eps,
            num_samples=EVAL_NUM_SAMPLES
        )
        accuracies[eps] = acc
        components[eps] = comps

    return accuracies, components


def stage5_eot(model, test_loader, epsilons):
    """
    Stage 5 — EOT-PGD. The headline measurement on Model 4.
    Returns dict {eps_raw → EOT robust accuracy (%)}.
    """
    print("\n" + "=" * 65)
    print(f"  Stage 5 — EOT-PGD on Model 4 (K={EOT_K_SAMPLES})")
    print("=" * 65)
    print(f"  WARNING: EOT-PGD is expensive — {EOT_K_SAMPLES} forwards per")
    print(f"  PGD step × {PGD_STEPS} steps × {PGD_RESTARTS} restarts per image.")
    print(f"  Budget several hours per epsilon at N={EVAL_NUM_SAMPLES}.")

    results = {}
    for eps in epsilons:
        eps_label = f"{round(eps*255)}/255"
        if eps == 0:
            results[eps] = _majority_vote_accuracy(
                model, test_loader, epsilon=0, num_samples=EVAL_NUM_SAMPLES
            )
            print(f"  epsilon = {eps_label}  ->  clean (maj. vote) = "
                  f"{results[eps]:.1f}%")
            continue

        eps_scaled = scale_epsilon(eps)
        print(f"\n── EOT-PGD at epsilon = {eps_label} ──────────────────────")

        acc = evaluate_eot_robust(
            model, test_loader, eps_scaled,
            num_samples=EVAL_NUM_SAMPLES,
            steps=PGD_STEPS,
            restarts=PGD_RESTARTS,
            k_samples=EOT_K_SAMPLES,
            n_eval=EOT_N_EVAL,
        )
        results[eps] = acc
        print(f"  EOT-PGD robust acc = {acc:.1f}%")

    return results


# ─────────────────────────────────────────────────────────────────────────────
#   Diagnostic helpers
# ─────────────────────────────────────────────────────────────────────────────

def _majority_vote_predict(model, images, n_eval=EOT_N_EVAL):
    """Majority-vote prediction across n_eval stochastic forwards."""
    model.eval()
    with torch.no_grad():
        num_classes = 10
        votes = torch.zeros(images.size(0), num_classes,
                            device=DEVICE, dtype=torch.long)
        for _ in range(n_eval):
            preds = model(images).argmax(dim=1)
            votes.scatter_add_(1, preds.unsqueeze(1),
                                torch.ones_like(preds.unsqueeze(1)))
        return votes.argmax(dim=1)


def _majority_vote_accuracy(model, loader, epsilon, num_samples,
                              n_eval=EOT_N_EVAL):
    """Clean (or fixed-perturbation) accuracy under majority vote."""
    model.eval()
    correct = 0
    total   = 0
    for images, labels in loader:
        if total >= num_samples:
            break
        remaining = num_samples - total
        images = images[:remaining].to(DEVICE)
        labels = labels[:remaining].to(DEVICE)

        # epsilon=0 path only — non-zero is handled inside the stage runner.
        assert epsilon == 0, "_majority_vote_accuracy expects epsilon=0"

        preds = _majority_vote_predict(model, images, n_eval=n_eval)
        correct += preds.eq(labels).sum().item()
        total   += labels.size(0)
    return 100.0 * correct / total


def check_d1_primary(model, test_loader, pgd_accuracies, epsilons):
    """
    D1 primary: FGSM > PGD at any epsilon ⇒ fire.
    On Model 4 this can fire because PGD is the one getting fooled by
    the stochastic gradients while FGSM (single step, sign-only) is
    less affected by the gradient direction noise.
    """
    print("\n── D1 primary (FGSM vs PGD on Model 4) ──────────────────")
    print(f"  {'Epsilon':<12} {'FGSM':>10} {'PGD':>10} {'Signal':>14}")
    print(f"  {'-'*48}")

    fired = False
    for eps in epsilons:
        if eps == 0:
            continue
        fgsm_acc = evaluate_fgsm(model, test_loader, eps)
        pgd_acc  = pgd_accuracies[eps]
        eps_label = f"{round(eps*255)}/255"
        flag      = "FIRED" if fgsm_acc < pgd_acc - 2.0 else "ok"
        if flag == "FIRED":
            fired = True
        print(f"  {eps_label:<12} {fgsm_acc:>9.1f}% {pgd_acc:>9.1f}% {flag:>14}")

    print(f"\n  D1 primary status: {'FIRED' if fired else 'clear'}")
    return fired


def check_d1_secondary(components, epsilons):
    """
    D1 secondary: Square Attack > APGD-CE (i.e. lower remaining accuracy
    after Square) ⇒ fire. Black-box outperforming white-box is the
    classic gradient-masking signal.
    """
    print("\n── D1 secondary (Square vs APGD-CE on Model 4) ──────────")
    fired = False
    for eps in epsilons:
        if eps not in components:
            continue
        c        = components[eps]
        apgd_acc = c.get("APGD-CE")
        sq_acc   = c.get("Square")
        eps_label = f"{round(eps*255)}/255"
        if apgd_acc is None or sq_acc is None:
            print(f"  eps={eps_label}: insufficient component data")
            continue
        if sq_acc < apgd_acc - 2.0:
            fired = True
            print(f"  eps={eps_label}: Square ({sq_acc:.1f}%) < APGD-CE "
                  f"({apgd_acc:.1f}%) → FIRED")
        else:
            print(f"  eps={eps_label}: APGD-CE dominant "
                  f"(APGD {apgd_acc:.1f}%, Square {sq_acc:.1f}%)")

    print(f"\n  D1 secondary status: {'FIRED' if fired else 'clear'}")
    return fired


def check_d4(model, test_loader, epsilon=8/255, steps=PGD_STEPS):
    """
    D4: PGD loss trajectory non-monotonic. On ME-Net the random mask
    redrawn between batches doesn't matter (single batch here), but
    the in-step stochasticity in the model's forward pass produces a
    bouncy trajectory.
    """
    print("\n── D4 (PGD loss trajectory on Model 4) ──────────────────")
    images, labels = next(iter(test_loader))
    images, labels = images.to(DEVICE), labels.to(DEVICE)

    traj = pgd_loss_trajectory(model, images, labels, epsilon, steps)
    drops = sum(1 for i in range(1, len(traj)) if traj[i] < traj[i-1])
    drop_pct = 100.0 * drops / (len(traj) - 1)

    print(f"  epsilon       : {round(epsilon*255)}/255")
    print(f"  steps         : {steps}")
    print(f"  loss drops    : {drops}/{steps-1} ({drop_pct:.1f}%)")
    print(f"  loss start    : {traj[0]:.4f}")
    print(f"  loss end      : {traj[-1]:.4f}")
    print(f"  loss max      : {max(traj):.4f}")

    fired = drop_pct > 20.0
    print(f"\n  D4 status: {'FIRED' if fired else 'clear'}")
    return fired, traj


# ─────────────────────────────────────────────────────────────────────────────
#   EOT diagnostic: the headline number
# ─────────────────────────────────────────────────────────────────────────────

def check_eot_gap(stage1_acc, stage5_acc, epsilons, threshold=30.0):
    """
    "EOT diagnostic" — the gap between Stage 1 (vanilla PGD) and
    Stage 5 (EOT-PGD) robust accuracies.

    A large gap (default threshold 30 percentage points) means the
    model's Stage 1 robust accuracy was a stochasticity artifact, not
    real robustness. This is the Tramèr §15 finding made into a
    pipeline-level diagnostic.

    Note: this is not D1–D7. The handoff notes Stage 5 itself produces
    the diagnostic; we report the gap directly rather than overload
    the existing D-numbering.
    """
    print("\n" + "=" * 65)
    print("  Stage 1 vs Stage 5 — the EOT diagnostic")
    print("=" * 65)
    print(f"  {'Epsilon':<12} {'Vanilla PGD':>13} {'EOT-PGD':>10} "
          f"{'Gap (pp)':>10}")
    print(f"  {'-'*48}")

    max_gap   = 0.0
    max_gap_e = None
    for eps in epsilons:
        if eps == 0:
            continue
        s1 = stage1_acc[eps]
        s5 = stage5_acc[eps]
        gap = s1 - s5
        if gap > max_gap:
            max_gap = gap
            max_gap_e = eps
        eps_label = f"{round(eps*255)}/255"
        print(f"  {eps_label:<12} {s1:>12.1f}% {s5:>9.1f}% {gap:>9.1f}")

    fired = max_gap > threshold
    print(f"\n  Max gap     : {max_gap:.1f}pp at "
          f"eps={round((max_gap_e or 0)*255)}/255")
    print(f"  Threshold   : {threshold:.1f}pp")
    print(f"  EOT signal  : {'FIRED' if fired else 'clear'}")
    return fired, max_gap


