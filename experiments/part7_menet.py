def plot_eot_gap(stage1_acc, stage5_acc, epsilons, save_path):
    eps_list   = [e for e in epsilons if e > 0]
    eps_labels = [f"{round(e*255)}/255" for e in eps_list]
    s1_vals    = [stage1_acc[e] for e in eps_list]
    s5_vals    = [stage5_acc[e] for e in eps_list]

    plt.figure(figsize=(8, 5))
    plt.plot(eps_labels, s1_vals, marker="o", linewidth=2,
             color="#C0392B", markersize=8,
             label="Vanilla PGD (stage 1)")
    plt.plot(eps_labels, s5_vals, marker="s", linewidth=2,
             color="#2E75B6", markersize=8,
             label=f"EOT-PGD (stage 5, K={EOT_K_SAMPLES})")

    for x, y in zip(eps_labels, s1_vals):
        plt.annotate(f"{y:.1f}%", (x, y),
                     textcoords="offset points", xytext=(0, 10),
                     ha="center", fontsize=9, color="#C0392B")
    for x, y in zip(eps_labels, s5_vals):
        plt.annotate(f"{y:.1f}%", (x, y),
                     textcoords="offset points", xytext=(0, -16),
                     ha="center", fontsize=9, color="#2E75B6")

    plt.title("Model 4 (ME-Net) — Stage 1 vs Stage 5\n"
              "Stochastic preprocessing → vanilla PGD is fooled, EOT recovers truth",
              fontsize=12)
    plt.xlabel("Perturbation budget (epsilon)", fontsize=11)
    plt.ylabel("Robust accuracy (%)", fontsize=11)
    plt.ylim(0, 100)
    plt.grid(True, alpha=0.3)
    plt.legend(fontsize=10)
    plt.tight_layout()
    plt.savefig(save_path, dpi=150)
    plt.close()
    print(f"  Plot saved to: {save_path}")


def plot_loss_trajectory(traj, d4_fired, epsilon, save_path):
    steps = list(range(1, len(traj) + 1))
    plt.figure(figsize=(8, 4))
    plt.plot(steps, traj, linewidth=1.2, color="#2E75B6")
    plt.title(
        f"Model 4 — PGD loss trajectory @ eps={round(epsilon*255)}/255\n"
        f"D4: {'FIRED' if d4_fired else 'clear'}",
        fontsize=12,
    )
    plt.xlabel("PGD step", fontsize=11)
    plt.ylabel("Cross-entropy loss", fontsize=11)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(save_path, dpi=150)
    plt.close()
    print(f"  Plot saved to: {save_path}")


