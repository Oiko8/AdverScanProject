def run():
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    print("=" * 65)
    print("  Part 7 — Model 4 (ME-Net) full pipeline")
    print(f"  ME-Net p_keep = {P_KEEP}, rank = {RANK}")
    print(f"  Samples : {EVAL_NUM_SAMPLES}")
    print(f"  Epsilons: {[f'{round(e*255)}/255' for e in EVAL_EPSILONS]}")
    print("=" * 65)

    if not os.path.isfile(CKPT_PATH):
        raise FileNotFoundError(
            f"Model 4 checkpoint not found at {CKPT_PATH}.\n"
            f"Train it first with:\n"
            f"    python -m models.train_menet {P_KEEP} {RANK}"
        )

    model = get_menet_model(CKPT_PATH, p_keep=P_KEEP, rank=RANK)
    _, test_loader = get_loaders()

    # ── Stage 1 (vanilla PGD) ────────────────────────────────────
    pgd_eps = [0/255] + EVAL_EPSILONS
    stage1_acc = stage1_pgd(model, test_loader, pgd_eps)

    # ── Stage 2 (AutoAttack) ─────────────────────────────────────
    aa_acc, components = stage2_autoattack(
        model, test_loader, EVAL_EPSILONS
    )

    # ── D1 primary, D1 secondary, D4 ────────────────────────────
    d1p = check_d1_primary(model, test_loader, stage1_acc, EVAL_EPSILONS)
    d1s = check_d1_secondary(components, EVAL_EPSILONS)
    d4, traj = check_d4(model, test_loader, epsilon=8/255, steps=PGD_STEPS)

    # ── Stage 5 (EOT-PGD) ────────────────────────────────────────
    stage5_acc = stage5_eot(model, test_loader, EVAL_EPSILONS)

    # ── Headline diagnostic: Stage 1 vs Stage 5 gap ─────────────
    eot_fired, max_gap = check_eot_gap(
        stage1_acc, stage5_acc, EVAL_EPSILONS, threshold=30.0
    )

    # ── Plots ────────────────────────────────────────────────────
    plot_eot_gap(
        stage1_acc, stage5_acc, EVAL_EPSILONS,
        save_path=os.path.join(OUTPUT_DIR, "part7_eot_gap.png"),
    )
    plot_loss_trajectory(
        traj, d4, epsilon=8/255,
        save_path=os.path.join(OUTPUT_DIR, "part7_d4_trajectory.png"),
    )

    # ── Report ──────────────────────────────────────────────────
    lines = []
    lines.append("=" * 65)
    lines.append("  Part 7 — Model 4 (ME-Net) full pipeline")
    lines.append("=" * 65)
    lines.append(f"  ME-Net config : p_keep={P_KEEP}, rank={RANK}")
    lines.append(f"  Samples       : {EVAL_NUM_SAMPLES}")
    lines.append(f"  EOT K         : {EOT_K_SAMPLES}")
    lines.append("")
    lines.append("  Stage 1 (vanilla PGD):")
    for eps in pgd_eps:
        eps_label = f"{round(eps*255)}/255"
        lines.append(f"    eps={eps_label:<7} -> {stage1_acc[eps]:5.1f}%")
    lines.append("")
    lines.append("  Stage 2 (AutoAttack):")
    for eps in EVAL_EPSILONS:
        eps_label = f"{round(eps*255)}/255"
        lines.append(f"    eps={eps_label:<7} -> {aa_acc[eps]:5.1f}%")
    lines.append("")
    lines.append("  Stage 2 component breakdown:")
    lines.append(f"    {'eps':<8} {'APGD-CE':>9} {'APGD-DLR':>10}"
                 f" {'FAB':>8} {'Square':>9}")
    for eps in EVAL_EPSILONS:
        c = components[eps]
        eps_label = f"{round(eps*255)}/255"
        def fmt(k):
            v = c.get(k)
            return f"{v:.1f}%" if v is not None else "N/A"
        lines.append(
            f"    {eps_label:<8} {fmt('APGD-CE'):>9} "
            f"{fmt('APGD-DLR'):>10} {fmt('FAB'):>8} {fmt('Square'):>9}"
        )
    lines.append("")
    lines.append("  Stage 5 (EOT-PGD):")
    for eps in EVAL_EPSILONS:
        eps_label = f"{round(eps*255)}/255"
        lines.append(f"    eps={eps_label:<7} -> {stage5_acc[eps]:5.1f}%")
    lines.append("")
    lines.append(f"  Stage1 vs Stage5 max gap : {max_gap:.1f}pp")
    lines.append(f"  EOT signal               : {'FIRED' if eot_fired else 'clear'}")
    lines.append("")
    lines.append("  Diagnostic summary on Model 4:")
    lines.append(f"    D1 primary       : {'FIRED' if d1p else 'clear'}")
    lines.append(f"    D1 secondary     : {'FIRED' if d1s else 'clear'}")
    lines.append(f"    D4               : {'FIRED' if d4 else 'clear'}")
    lines.append(f"    EOT (S1 vs S5)   : {'FIRED' if eot_fired else 'clear'}")
    lines.append("=" * 65)

    summary = "\n".join(lines)
    print("\n" + summary)
    save_report("part7_menet.txt", summary)


if __name__ == "__main__":
    run()